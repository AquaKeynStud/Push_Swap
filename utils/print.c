#include <unistd.h>

int	print(char *s)
{
	int	i;
	int	success;

	i = 0;
	while (s[i])
		i++;
	if (!s)
		return (-1);
	success = write(1, s, i);
	if (success == -1)
		return (-1);
	return (1);
}
