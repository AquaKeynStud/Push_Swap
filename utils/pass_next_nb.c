int	pass_next_nb(char *s, int passed)
{
	int	i;

	i = 0;
	while (*s == ' ' || *s == '-' || *s == '+')
	{
		i++;
		s++;
	}
	while (*s >= '0' && *s <= '9')
	{
		i++;
		s++;
	}
	return (i - passed);
}
